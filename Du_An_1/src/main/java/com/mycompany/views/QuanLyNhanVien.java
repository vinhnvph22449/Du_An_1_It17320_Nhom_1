		String name = textName.getText();
		person.setName(name);
		Boolean gioiTinh = rdbtnNam.isSelected();
		person.setGender(gioiTinh);
		String sdt = textSDT.getText();
		person.setPhoneNumber(sdt);
		Date ngaySinh = (Date) spinnerDate.getValue();
		person.setDateOfBirth(ngaySinh);
		String email = textEmail.getText();
		person.setEmail(email);
		String diaChi = textDiaChi.getText();
		person.setAddress(diaChi);
		String userNmae = textUserName.getText();
		String password = pwMatKhau.getText();
		employee.setPerson(person);
		
		employee.setUsername(userNmae);
		employee.setPassword(password);
		RoleServiceImpl roleServiceImpl = new RoleServiceImpl();
		roleServiceImpl.findAll().get(0).getId();
		employee.setRole(roleServiceImpl.findAll().get(0));
		return employee;
