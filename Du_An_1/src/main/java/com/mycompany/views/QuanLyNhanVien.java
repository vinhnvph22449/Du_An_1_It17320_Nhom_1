    private void btnUpdateActionPerformed() {
    	Employee employee =  getFormData();
		if (employee!=null) {
			try {
				String id = lblID.getText();
				employee.setId(UUID.fromString(id));
				new EmployeeServiceImpl().update(employee);
				JOptionPane.showMessageDialog(this, "Thành công");
			} catch (Exception e) {
				JOptionPane.showMessageDialog(this, "lôi");
			}
		}
	}

	private void choseAvate() {

		JFileChooser fileChooser = new JFileChooser();
		fileChooser.setFileFilter(new FileFilter() {
			@Override
			public String getDescription() {
				return "Images file (.jpg)";
			}

			@Override
			public boolean accept(File f) {
				if (f.isDirectory()) {
					return true;
				} else {
					return f.getName().toLowerCase().endsWith(".jpg");
				}
			}
		});
		if (fileChooser.showDialog(this, "Chọn file") == fileChooser.APPROVE_OPTION) {
			File file = fileChooser.getSelectedFile();
			if (file.exists()) {
				ImageIcon icon = new ImageIcon(file.getPath());
				Image image = ImageUtil.resize(icon.getImage(), 130, 143);
				ImageIcon imageIcon = new ImageIcon(image);
//				try {
//					pertionImage = ImageService.toByteArray(icon.getImage(), "jpg");
//				} catch (IOException e1) {
//					e1.printStackTrace();
//				}
				lblAvata.setIcon(imageIcon);
			} else {
				JOptionPane.showMessageDialog(this, "File không tồn tại");
			}
		} else {
			return;
		}

	
		
	}

